                <!-- History -->
                <div class="history-block">
                    <h2><i class="fas fa-history" style="margin-right: 12px; opacity:0.8;"></i>History of GCEE (Formerly IRTT)</h2>
                    <p>The Institute commenced operations on 10th October 1984 as an Automobile Research-oriented Institution under the aegis of the Institute of Road Transport (IRT), Taramani, Chennai, and the State Transport Undertakings (STUs), as per G.O. Ms. No. 479 issued by the Department of Science and Technology on 26th April 1984.</p>
                    <p>Established with the noble vision of delivering quality engineering education to the underprivileged and economically weaker sections of society, the Institute is unique in its mission and foundation.</p>
                    <p>At inception, the Institute admitted 120 students, with 30 students in each of the following disciplines:</p>
                    <ul class="history-list">
                        <li>Automobile Engineering</li>
                        <li>Computer Technology &amp; Informatics</li>
                        <li>Mechanical Engineering</li>
                        <li>Transportation Engineering</li>
                    </ul>
                    <p>The intake was increased from 120 to 180 from the academic year 1986-87 and 210 from 1992-93, 300 from 1994-95, 360 from 2002-03 and 420 from 2012-13. Now, the Institute offers eight engineering disciplines at under graduate level and three programs at the post graduate level.</p>
                    <p>In 2021, the college was transferred to under the governance of Directorate of Technical Education (DoTE) and subsequently renamed as Government College of Engineering, Erode. It is a premier Government Engineering College affiliated to Anna University, Chennai. The Institute is located on a sprawling lush green campus of 200 acres between Chithode and Bhavani in Erode district.</p>
                    <p>This Institute is functioning as the Zonal Headquarters for Zone-XI (Erode Zone) of Anna University monitoring the Engineering colleges affiliated to Anna University in Erode district and part of Namakkal, Tiruppur and Salem districts.</p>
