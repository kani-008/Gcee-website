                    <h2>GCEE at a Glance</h2>
                    <p>The college was established in 1984 as an Automobile Research Oriented Engineering college under Institute of Road and Transport by the Tamil Nadu State Transport Corporation. On 26 August 2021, Higher Education Minister of Tamil Nadu announced on Tamil Nadu Legislative Assembly that the IRTT was taken up by the government to directly run under the governance of Directorate of Technical Education (DoTE) and subsequently renamed as Government Engineering College, Erode.</p>
                    <p>It is a premier Government Engineering College affiliated to Anna University, Chennai. It is located in a sprawling lush green campus of 200 acres. It provides a congenial atmosphere for Technical Education. It is 15 km from Erode Railway Station and half a kilometer from the NH-47 between Bhavani and Chithode.</p>
                    <p>The Institute offers 8 Under Graduate Degree courses in Engineering in various disciplines and 2 post graduate courses – M.E. Structural Engineering and M.E. Computer Science &amp; Engineering. The Institute has excellent infrastructure, well-equipped laboratories, library and highly qualified faculty members. The Institute is well known for its technical excellence, modern facilities, good performance track record and resourcefulness.</p>
